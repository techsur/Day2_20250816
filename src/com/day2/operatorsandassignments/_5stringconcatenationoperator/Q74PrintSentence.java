package com.day2.operatorsandassignments._5stringconcatenationoperator;
//74. Program to print a sentence using multiple strings and numbers with +.
public class Q74PrintSentence {

	public static void main(String[] args) {
		String name = "Alice";
		int age = 25;
		String city = "Paris";

		// Concatenate strings and numbers using +
		String sentence = "My name is " + name + ", I am " + age + " years old, and I live in " + city + ".";

		// Print the sentence
		System.out.println(sentence);
	}
}
